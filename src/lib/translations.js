function normalizeWhitespace(value) {
  return value.replace(/\s+/g, " ").trim();
}

function preserveOuterWhitespace(original, translated) {
  const prefixMatch = original.match(/^\s*/);
  const suffixMatch = original.match(/\s*$/);
  const prefix = prefixMatch ? prefixMatch[0] : "";
  const suffix = suffixMatch ? suffixMatch[0] : "";
  return `${prefix}${translated}${suffix}`;
}

const RW_TRANSLATIONS = new Map(
  Object.entries(RAW_RW_TRANSLATIONS).map(([key, value]) => [normalizeWhitespace(key), value]),
);

export function translateAppText(value, language = "en") {
  if (typeof value !== "string" || !value.trim() || language !== "rw") {
    return value;
  }

  const normalized = normalizeWhitespace(value);
  const direct = RW_TRANSLATIONS.get(normalized);
  if (direct) {
    return preserveOuterWhitespace(value, direct);
  }

  for (const entry of RW_DYNAMIC_TRANSLATIONS) {
    const match = normalized.match(entry.pattern);
    if (match) {
      return preserveOuterWhitespace(value, entry.translate(match));
    }
  }

  return value;
}
